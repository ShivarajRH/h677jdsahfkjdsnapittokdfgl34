<h3>Ordering</h3>
<b>How will I know if you have received my order?</b><br>
Once your payment has been accepted, you will also get an email in your registered email id confirming the order along with all the details such as the transaction number and the list of items ordered. 
<br><br>
<b>How do I check my order status?</b><br>
We normally dispatch your order within 48 hrs. The name of the courier service used and the tracking number of your consignment will be sent to your registered email address. In case you do not receive a mail from us within 48 hrs of placing an order please check your spam folder. Tracking may not appear online for up to another 24 hours in some cases, so please wait until your package is scanned by the courier company. Alternatively, you can check the same in My Profile page, all relevant shipping details will be mentioned for your easy reference. 
<br><br>
<b>How safe is online shopping at Snapittoday.com?</b><br>
We are partnered with EBS - a Verisign secured payment gateway. You information is completely safe and secure.
<br><br>
<b>What if an item is out of stock?</b><br>
We make every effort to keep all products in stock. However, there may be times when unexpected demands exceed our supply. In the rare circumstance where the order has been accepted and due to some reasons we are not able to ship, we will notify you within 48 hrs and the money will be returned to you in the same mode by which the payment was made within 10 working days. Some items (which are restocked regularly) and is temporarily out of stock may appear on display with an out of stock tag. 
<br><br>
<b>Will I be charged for Octroi or Entry tax?</b><br>
You will get the final price during check out. Our prices are all inclusive and you need not pay anything extra. 
<br><br>
<h3>Shipping</h3>
<b>How long will it take to receive my order?</b><br>
For all areas serviced by reputed couriers, the delivery time would be within 3 to 4 business days after dispatch (Sundays and other holidays are not accounted). However items weighing over 2 kilos or high volume may take a couple of days longer to reach. For other areas the products will be shipped through Indian Postal Service and may take 1-2 weeks depending on location. 
<br><br>
<b>Can I ship different items in my order to different shipping addresses?</b><br>
We can only process one shipping address per order. So, if you would like to order several items and ship to different people, please treat these as separate orders. 
<br><br>
<b>Who pays for the shipping charges?</b><br>
We provide Free Shipping on all Orders above Rs 400. For Orders below Rs 400, shipping charges of Rs 40 will apply.<br> 
In case of Cash On Delivery (COD), a service fee of Rs 40 is added regardless of the total Order amount.<br>
<br>
<b>What happens if I am not available at the time of delivery?</b><br>
Our delivery partners will attempt to contact you & deliver the package thrice before it is returned back to our warehouse. In case of no response, the package will be returned to us. An extra fee of Rs 50 will be levied in order to ship the package again.
<br><br>
<h3>Cancellation & Refund</h3>
<b>What about return policies & Refunds?</b><br>
Please ensure that while returning the items are packed properly so that the original packing of the product is not damaged in transit. The return shipping charges needs to be borne by the customer in case of undamaged goods. Your order amount will be refunded on verification of the goods returned. In case of damage to the original packaging or the goods Snapittoday.com reserves the right to determine a fair value of the product on return and the same shall be binding on both parties. Please send us a mail at hello@Snapittoday.com or call us on 080-42124462 to obtain a Return Receipt Number before you ship the item back to us. No goods will be accepted without a Return Receipt Number. 
<br><br>
For goods bought on discount the return will be through Snapittoday.com Gift coupons.
<br><br>
For goods bought on MRP, we will refund the money the way the payment was made.<br> 
The refund process will be initiated once we have received the product(s). Typically refunds are processed in less than 10 working days. The refunded amount will take 7-10 bank working days to be reflected in your account.
<br><br>
All products sold at Snapittoday.com is sourced from genuine Brand dealers. For products which are incorrectly shipped, damaged in transit, with defects please send us a mail at customercare@Snapittoday.com or call us on hello@Snapittoday.com or call us on 080-42124462. We will pay the return shipping charges and will send a replacement within 10 working days. We reserve the right to pay you back in case there are no stocks left of the item returned.
